package section7OOPCompositionEncapsPoly;

public class A1Dimensions90 {
	private int width;
	private int height;
	private int depth;
	public A1Dimensions90(int width, int height, int depth) {
		super();
		this.width = width;
		this.height = height;
		this.depth = depth;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	public int getDepth() {
		return depth;
	}
	
}
